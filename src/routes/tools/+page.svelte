	<h2 id="mismatched">…and two things I bought that don't suit fine formalwear</h2>
	<div class="card preset-outlined-warning-500 not-prose my-4 p-4 text-sm">
		<ul class="m-0 list-disc space-y-2 pl-5">
			<li>
				<strong>Bonded-nylon upholstery thread</strong> — too heavy and stiff for shirting and suiting; it puckers seams
				and the nylon can <em>melt</em> under a wool press. Keep it for heavy structural repairs; buy fine all-purpose polyester
				for garment seams.
			</li>
			<li>
				<strong>2″ steel T-pins</strong> — too thick for shirting and lining (they leave holes and snag). Reserve them for
				the dress form and heavy coat layers; buy fine 0.5&nbsp;mm glass-head silk pins for the actual garments.
			</li>
		</ul>
	</div>

	<h2 id="needed">The priority gap: pressing equipment</h2>
	<p>
		A weak iron physically cannot shape wool. Strong steam plus iron weight is what molds suiting into a feminine curve
		— shrinking out side-seam fullness, setting a tapered crease, flattening a bulky taken-in seam so it doesn't show.
		In rough priority:
	</p>
	<ul>
		<li>
			<strong>Tailoring steam iron</strong> — e.g. a Rowenta DW5280 Steamforce or DW8080 Pro Master class (~$90–120); a gravity-feed
			or steam-generator station if the budget stretches (~$200–300). Avoid cheap dry/travel irons.
		</li>
		<li>
			<strong>A sturdy wide board or pressing table + a wool pressing mat</strong> — a firm, heat-stable surface; flimsy boards
			bleed off steam (~$60–90 board + ~$30–45 mat).
		</li>
		<li>
			<strong>Tailor's ham + seam/sleeve roll</strong> — presses curved darts, shaped sides and sleeve caps into a 3-D shape
			instead of flattening them. This is what builds the hourglass and the soft shoulder roll.
		</li>
		<li>
			<strong>Sleeve board</strong> (~$25) and a <strong>wooden clapper / point-presser</strong> (~$15–25) — the clapper
			bangs steam-set creases flat in heavy wool; cooling-under-pressure is what <em>sets</em> wool.
		</li>
		<li>
			<strong>Press cloth</strong> (silk organza + cotton) — mandatory on wool/dark/fused fabric to prevent shine and scorch.
		</li>
		<li>
			<strong>Pinking shears or a pinking-blade rotary head</strong> — the only quick no-serger fray-resist edge besides a
			hand overcast or a Hong-Kong bind (the straight rotary won't pink).
		</li>
	</ul>

	<h2 id="consumables">Consumables to buy</h2>
	<ul>
		<li>
			<strong>Thread:</strong> fine all-purpose polyester (Gütermann Sew-All / Coats Dual Duty) in black, navy, charcoal,
			grey, white; silk basting thread (pulls out clean, leaves no mark when pressed over).
		</li>
		<li>
			<strong>Pins &amp; marking:</strong> fine 0.5&nbsp;mm glass-head pins; tailor's chalk + a chalk wheel and wax chalk
			(melts into wool); a tracing wheel + carbon and silk thread for tailor's tacks on fabrics chalk won't show on.
		</li>
		<li>
			<strong>Needles:</strong> hand — sharps, milliners, betweens + a curved needle; machine — Universal 80/12 &amp; 90/14
			(suiting), Microtex 70/10–80/12 (shirting), Jeans/Denim 90/14–100/16 + Topstitch (waistbands), Ballpoint 75/11 (knit
			linings). The treadle 15/66/201 take 15×1/HA×1; 127/128 take round-shank 127×1.
		</li>
		<li>
			<strong>Structure:</strong> cotton twill / fusible stay tape (stays the suppressed waist and shoulder seams so they
			don't re-stretch); fusible weft/tricot + a little sew-in hair canvas to re-support reshaped collars, cuffs, lapels and
			waistbands; beeswax/thread conditioner for hand work.
		</li>
		<li>
			<strong>Finishing:</strong> bias / Hong-Kong binding (or spare lining) + Fray-Check; spare bobbins (class 15/15J for
			the 6600C); spare suit buttons + a set of thin shoulder pads.
		</li>
		<li><strong>Measuring:</strong> a flexible tape measure, a clear gridded ruler, and a seam gauge.</li>
	</ul>

